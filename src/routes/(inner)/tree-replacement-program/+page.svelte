	<ol>
		<li>
			<p>
				<span>
					The refund only applies to ash trees located in the right of way (within 15’ from the
					street)
				</span>
				<span>that have been removed and replanted with an appropriate shade tree.</span>
			</p>
		</li>
		<li>
			<p>
				<span>
					The replacement tree doesn’t have to be planted in the exact spot as the ash tree removed
					but
				</span>
				<span>must be planted back in the right of way, so it stays a “street tree.”</span>
			</p>
		</li>
		<li>
			<p>
				<span>
					The replacement tree must be a “shade tree.” Shade trees are varieties that must reach a
				</span>
				<span>
					minimum height of 35’ at maturity, has a more rounded shaped canopy, is not weak wooded
				</span>
				<span>
					and does well in this region or planting zone 6. This includes varieties such as Oaks,
					Maples,
				</span>
				<span>
					Black Gums, London Planetree, Ginko, Elm, Bald Cypress, etc. Basically, we are looking for
				</span>
				<span>
					trees that get very large in size that will create a canopy over the roadway at some
					point.
				</span>
				<span>
					Upright or columnar trees and ornamental’s such as redbuds and crabapples will not be
				</span>
				<span>eligible for the refund.</span>
			</p>
		</li>
		<li>
			<p>
				<span>
					Once the planting is completed, residents can email Curt Talken a picture of any receipts
				</span>
				<span>
					associated to planting. Planting expenses include the cost of the tree, contractor labor
					to plant
				</span>
				<span>it if applicable, mulch, staking materials, etc.</span>
			</p>
		</li>
		<li>
			<p>
				<span>
					Assuming all requirements have been met, a refund of 50% of those costs up to $200 will be
				</span>
				<span>
					mailed to the resident. This refund is per property and not per tree. This is to help as
					many
				</span>
				<span>
					property owner’s as possible since we have limited funding for this program. Refund does
				</span>
				<span>not include tree removal costs.</span>
			</p>
		</li>
		<li>
			<p>
				<span>
					In addition, if you have previously removed an ash tree located in the right of way, you
					can
				</span>
				<span>
					still be eligible for a refund if you would like to replant with an appropriate shade
					tree. We
				</span>
