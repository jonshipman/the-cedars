<Title>Welcome New Residents!</Title>

<p>
	We are sure you will find The Cedars a great place to live. There are 197 homes which are a part
	of The Cedars Homes Association. Adjacent west is an older neighborhood also called the Cedars
	which is not part of our Homes Association.
</p>
<p>
	The Cedars presently has a nine-member Board of Directors. All residents are encouraged to attend
	the board meetings. Please RSVP the Board President if you plan to attend.
</p>
<p>
	The Cedars Homes Association annual meeting is the first Thursday in November of each year. We
	elect board members and approve the budget. A special notice will be delivered to you prior to
	these events.
</p>
<p>
	The annual dues include maintenance of common areas, weekly trash pickup, socials and
	miscellaneous expenses. There is a contingency fund set aside to take care of major repairs such
	as the 87th street fence.
</p>
